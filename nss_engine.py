def nelson_siegel(tau, beta0, beta1, beta2, lamba):
    """
    Calculates the yield for maturity tau based on Nelson-Siegel parameters.
    Beta0: Level, Beta1: Slope, Beta2: Curvature, Lamba: Decay factor
    """
    # Handle tau=0 to avoid division by zero
    tau = np.maximum(tau, 1e-6)

    component1 = (1 - np.exp(-lamba * tau)) / (lamba * tau)
    component2 = component1 - np.exp(-lamba * tau)

    yield_val = beta0 + (beta1 * component1) + (beta2 * component2)
    return yield_val

def objective_function(params, maturities, yields):
    """Sum of squared residuals for calibration."""
    beta0, beta1, beta2, lamba = params
    predictions = nelson_siegel(maturities, beta0, beta1, beta2, lamba)
    return np.sum((yields - predictions)**2)

def calibrate_yield_curve(maturities, yields):
    """Calibrates NS parameters for a single day."""
    # Initial guess: [Level (avg yield), Slope (long-short), Curvature, Lambda]
    initial_guess = [yields.iloc[-1], yields.iloc[-1] - yields.iloc[0], 0, 1.0]

    res = minimize(objective_function, initial_guess, args=(maturities, yields),
                   method='Nelder-Mead', tol=1e-6)
    return res.x if res.success else [np.nan]*4

