class FREDDataEngine:
    """Handles institutional-grade data retrieval and cleaning from FRED."""
    TICKERS = {
        'DGS1MO': 1/12, 'DGS3MO': 3/12, 'DGS6MO': 6/12,
        'DGS1': 1, 'DGS2': 2, 'DGS3': 3, 'DGS5': 5,
        'DGS7': 7, 'DGS10': 10, 'DGS20': 20, 'DGS30': 30
    }

    def __init__(self, years_back=5):
        self.start_date = datetime.now() - timedelta(days=years_back*365)
        self.maturities = np.array(list(self.TICKERS.values()))

    def fetch_weekly_data(self):
        try:
            df = web.DataReader(list(self.TICKERS.keys()), 'fred', self.start_date)
            df = df.ffill().dropna()
            df['Spread_10Y2Y'] = df['DGS10'] - df['DGS2']
            weekly_df = df.resample('W').last()
            return weekly_df
        except Exception as e:
            raise ConnectionError(f"FRED API Error: {e}")

class NSSCurveModel:
    """Advanced Nelson-Siegel-Svensson Engine with Regularization."""
    def __init__(self, l2_lambda=0.005):
        self.l2_lambda = l2_lambda

    @staticmethod
    def nss_formula(tau, b0, b1, b2, b3, l1, l2):
        tau = np.maximum(tau, 1e-6)
        term1 = (1 - np.exp(-l1 * tau)) / (l1 * tau)
        term2 = term1 - np.exp(-l1 * tau)
        term3 = ((1 - np.exp(-l2 * tau)) / (l2 * tau)) - np.exp(-l2 * tau)
        return b0 + b1*term1 + b2*term2 + b3*term3

    def calibrate(self, yields, maturities, last_params=None):
        def objective(params):
            b0, b1, b2, b3, l1, l2 = params
            preds = self.nss_formula(maturities, b0, b1, b2, b3, l1, l2)
            mse = np.mean((yields - preds)**2)
            penalty = self.l2_lambda * (b2**2 + b3**2)
            return mse + penalty

        bounds = [(0, 15), (-15, 15), (-20, 20), (-20, 20), (0.2, 5.0), (0.2, 5.0)]
        init_guess = last_params if last_params is not None else [yields.iloc[-1], yields.iloc[-1]-yields.iloc[0], 0, 0, 1.0, 2.0]
        res = minimize(objective, init_guess, method='L-BFGS-B', bounds=bounds, tol=1e-7)
        return res.x if res.success else np.full(6, np.nan)

class QuantDashboard:
    """Advanced Macro Portfolio Dashboard with Corrected Regime Classification."""
    def __init__(self, df_params, df_rates, maturities):
        self.df_params = df_params
        self.df_rates = df_rates
        self.maturities = maturities

    def classify_regime(self, slope_param):
        # In NS: Spread (L-S) = -Beta1.
        # Normal (Positive Spread) -> Negative Beta1
        # Inverted (Negative Spread) -> Positive Beta1
        if slope_param < -0.5: return ("Expansionary (Steep)", "#00FF00")
        elif slope_param > 0.1: return ("Recession Warning (Inverted)", "#FF4136")
        else: return ("Transition (Flat)", "#AAAAAA")

    def render(self):
        curr_beta1 = self.df_params['Beta1'].iloc[-1]
        regime_name, regime_color = self.classify_regime(curr_beta1)

        fig = make_subplots(
            rows=2, cols=2,
            specs=[[{"type": "xy"}, {"type": "xy"}],
                   [{"type": "surface", "colspan": 2}, None]],
            subplot_titles=("Curve Analysis: Today vs 1M Ago", f"Regime Tracking (-Beta1 vs 10Y2Y)", "3D Term Structure Evolution")
        )

        # 1. 2D Curve (Top Left) - Restore 1M comparison
        today = self.df_rates.drop(columns=['Spread_10Y2Y']).iloc[-1]
        one_month_ago = self.df_rates.drop(columns=['Spread_10Y2Y']).iloc[-5] if len(self.df_rates) > 5 else self.df_rates.iloc[0]
        fig.add_trace(go.Scatter(x=self.maturities, y=today, name='Live Curve', line=dict(width=4, color='cyan')), row=1, col=1)
        fig.add_trace(go.Scatter(x=self.maturities, y=one_month_ago, name='1M Ago', line=dict(dash='dot', color='gray')), row=1, col=1)

        # 2. Factor Evolution & Regime Shading (Top Right) - Plot -Beta1
        implied_slope = -self.df_params['Beta1']
        for i in range(len(self.df_params)-1):
            if self.df_params['Beta1'].iloc[i] > 0.1: # Inverted
                fig.add_vrect(x0=self.df_params.index[i], x1=self.df_params.index[i+1],
                              fillcolor="red", opacity=0.2, layer="below", line_width=0, row=1, col=2)

        fig.add_trace(go.Scatter(x=self.df_params.index, y=implied_slope, name='-Beta1 (Model Slope)', line=dict(color='orange', width=3)), row=1, col=2)
        fig.add_trace(go.Scatter(x=self.df_rates.index, y=self.df_rates['Spread_10Y2Y'], name='10Y-2Y (Bench)', line=dict(color='white', dash='dot', width=1)), row=1, col=2)

        # 3. 3D Surface (Bottom)
        plot_mats = np.linspace(0.1, 30, 40)
        z_matrix = np.array([NSSCurveModel.nss_formula(plot_mats, *p) for p in self.df_params.values])
        fig.add_trace(go.Surface(z=z_matrix, x=plot_mats, y=self.df_params.index, colorscale='Viridis', showscale=False), row=2, col=1)

        fig.add_annotation(dict(xref="paper", yref="paper", x=0.5, y=1.1, text=f"CURRENT REGIME: <span style='color:{regime_color}'>{regime_name.upper()}</span>", showarrow=False, font=dict(size=18, color="white")))
        fig.update_layout(height=950, width=1200, template="plotly_dark", margin=dict(t=100))
        fig.show()

